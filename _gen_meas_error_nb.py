    "        normalized = normalized[2:]\n",
    "    try:\n",
    "        return (0, int(normalized, 16), str(serial))\n",
    "    except ValueError:\n",
    "        return (1, str(serial))\n",
    "\n",
    "\n",
    "def get_first_available_channel(group, channel_names):\n",
    "    \"\"\"Return data from the first channel name that exists in *group*.\"\"\"\n",
