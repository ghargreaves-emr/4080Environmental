CELL_IMPORTS = [
    "import os\n",
    "import re\n",
    "import base64\n",
    "from datetime import datetime\n",
    "\n",
    "import pandas as pd\n",
    "from nptdms import TdmsFile\n",
    "import plotly.graph_objects as go\n",
    "import plotly.colors as pc\n",
    "\n",
    "\n",
    "def parse_filename(filename):\n",
    "    \"\"\"Parse TDMS filename into (serial, temp_set, rh_set, datetime).\n",
    "\n",
    "    Expected pattern (spaces normalised):\n",
    "      <SERIAL>_Temp_Set_<TEMP>_RH_Set_<RH>_MM_DD_YYYY_HH_MM_SS_AM|PM.tdms\n",
    "    \"\"\"\n",
    "    name = filename[:-5].strip()\n",
    "    name = re.sub(r'\\s+', ' ', name)\n",
    "    name = re.sub(r'\\s+(AM|PM)$', r'_\\1', name)\n",
    "    name = name.replace('RH_Set_ ', 'RH_Set_')\n",
    "\n",
    "    pattern = re.compile(\n",
    "        r'^(?P<serial>[^_]+)_Temp_Set_(?P<temp>[^_]+)_RH_Set_(?P<rh>\\d+)_'\n",
    "        r'(?P<month>\\d+)_(?P<day>\\d+)_(?P<year>\\d+)_'\n",
    "        r'(?P<hour>\\d+)_(?P<minute>\\d+)_(?P<second>\\d+)_(?P<am_pm>AM|PM)$'\n",
    "    )\n",
    "    match = pattern.match(name)\n",
    "    if not match:\n",
    "        raise ValueError(f'Unable to parse filename: {filename}')\n",
    "\n",
    "    serial   = match.group('serial')\n",
    "    temp_set = match.group('temp')\n",
    "    rh_set   = match.group('rh')\n",
    "    month    = int(match.group('month'))\n",
    "    day      = int(match.group('day'))\n",
    "    year     = int(match.group('year'))\n",
    "    hour     = int(match.group('hour'))\n",
    "    minute   = int(match.group('minute'))\n",
    "    second   = int(match.group('second'))\n",
    "    am_pm    = match.group('am_pm')\n",
    "\n",
    "    dt = datetime(year, month, day, hour, minute, second)\n",
    "    if am_pm == 'PM' and hour != 12:\n",
    "        dt = dt.replace(hour=hour + 12)\n",
    "    elif am_pm == 'AM' and hour == 12:\n",
    "        dt = dt.replace(hour=0)\n",
    "    return serial, temp_set, rh_set, dt\n",
]

