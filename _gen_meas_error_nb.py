CELL_LOAD = [
    "data = []\n",
    "\n",
    "if not os.path.exists(ROOT_PATH):\n",
    "    raise FileNotFoundError(f'Path not found: {ROOT_PATH}  \u2014 please update ROOT_PATH.')\n",
    "\n",
    "# Collect every .tdms file up front so we can report progress while loading.\n",
    "tdms_files = []\n",
    "for subdir in os.listdir(ROOT_PATH):\n",
    "    serial_path = os.path.join(ROOT_PATH, subdir)\n",
    "    if not os.path.isdir(serial_path):\n",
    "        continue\n",
    "    for file in os.listdir(serial_path):\n",
    "        if file.endswith('.tdms'):\n",
    "            tdms_files.append((serial_path, file))\n",
    "\n",
    "total_files = len(tdms_files)\n",
    "print(f'Found {total_files} TDMS file(s) to process.')\n",
    "\n",
    "for idx, (serial_path, file) in enumerate(tdms_files, start=1):\n",
    "    print(f'\\r[{idx}/{total_files}] Loading {file[:60]}', end='', flush=True)\n",
    "    filepath = os.path.join(serial_path, file)\n",
    "    try:\n",
    "        parsed_serial, temp_set, rh_set, timestamp = parse_filename(file)\n",
    "        tdms_file = TdmsFile.read(filepath)\n",
    "        group = tdms_file.groups()[0]\n",
    "\n",
    "        ranges_config = [\n",
    "            ('10V',  ['Test Points 10V'],                ['DUT Measurements 10V']),\n",
    "            ('300V', ['Test Points 300V', 'Test Points'], ['DUT Measurements 300V', 'DUT Measurements']),\n",
    "        ]\n",
    "\n",
    "        for range_name, test_candidates, dut_candidates in ranges_config:\n",
    "            test_col = next((c for c in test_candidates if c in group), None)\n",
    "            dut_col  = next((c for c in dut_candidates  if c in group), None)\n",
    "            if test_col is None or dut_col is None:\n",
    "                continue\n",
    "\n",
    "            test_pts = group[test_col][:]\n",
    "            dut_meas = group[dut_col][:]\n",
    "            divisor  = RANGE_DIVISORS[range_name]\n",
    "\n",
    "            for src, dut in zip(test_pts, dut_meas):\n",
    "                error_ppm = (float(dut) - float(src)) / divisor * 1_000_000\n",
    "                data.append({\n",
    "                    'timestamp':      timestamp,\n",
    "                    'serial':         parsed_serial,\n",
    "                    'range':          range_name,\n",
    "                    'source_voltage': float(src),\n",
    "                    'meas_error_ppm': error_ppm,\n",
    "                })\n",
    "\n",
    "    except Exception as e:\n",
    "        print(f'\\nError processing {filepath}: {e}')\n",
    "\n",
    "print(f'\\nDone. Loaded {len(data):,} sample(s) from {total_files} file(s).')\n",
    "\n",
    "df = pd.DataFrame(data)\n",
    "if df.empty:\n",
    "    raise ValueError('No data loaded \u2014 check ROOT_PATH and TDMS file names.')\n",
    "\n",
    "df = df.sort_values(['serial', 'timestamp', 'source_voltage']).reset_index(drop=True)\n",
    "\n",
    "summary = df.groupby(['serial', 'range']).agg(\n",
    "    sweeps=('timestamp', 'nunique'),\n",
    "    samples=('meas_error_ppm', 'count')\n",
    ")\n",
    "print('\\nData summary (sweeps = unique timestamps):')\n",
    "print(summary.to_string())\n",
]

